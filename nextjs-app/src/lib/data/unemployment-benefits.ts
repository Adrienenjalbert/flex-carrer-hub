export interface StateUnemploymentInfo {
  name: string;
  abbreviation: string;
  // Benefit calculation
  maxWeeklyBenefit: number;
  minWeeklyBenefit: number;
  replacementRate: number; // Percentage of wages replaced (typically 50-60%)
  maxWeeks: number; // Standard duration
  // Base period calculation method
  calculationMethod: 'highest-quarter' | 'two-highest-quarters' | 'average-weekly' | 'annual';
  calculationDivisor: number; // e.g., 26 means HQ/26
  // Partial unemployment
  partialEarningsDisregard: number; // Amount you can earn before benefits reduce
  partialEarningsDisregardType: 'flat' | 'percentage' | 'greater-of';
  partialEarningsDisregardPercent?: number; // If percentage-based
  benefitReductionRate: number; // How much benefit reduces per dollar earned (usually 1:1 or 0.5:1)
  // Gig work rules
  allowsPartTimeWork: boolean;
  mustSeekFullTime: boolean;
  gigWorkImpact: 'reduces-benefits' | 'may-disqualify' | 'allowed-with-reporting';
  // Additional info
  waitingWeek: boolean; // Most states have 1-week unpaid waiting period
  dependentAllowance: number; // Extra per dependent (0 if none)
  maxDependents: number;
  // Base period requirements
  minBaseWages: number; // Minimum wages in base period to qualify
  minWeeksWorked: number; // Minimum weeks of work required
  // Special notes
  notes: string;
}

